"""g3_reverse_sat_alpha_structure.py — analyze the 9 saturating α_1 values in
Reverse Pattern (15, 18, 19) at (32, 8). Test:

  (a) Are they roots of a low-degree polynomial P_f(α_1)?
  (b) Is that polynomial a trinomial / Bluher-shaped?
  (c) Does the set form a coset / additive subgroup / multiplicative coset?
  (d) Do they correspond to specific (f_e)_o + α_1·(f_o)_o structures?

The goal is to pin down a CLOSED-FORM polynomial whose roots are |B_1| ≤ 9
under recursive above-J — bridging to Bluher's Hilbert-90 framework.
"""
import sys, os, random
